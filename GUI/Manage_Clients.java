    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        try {
            try {
                Connection con = DataBaseConnection.getDBconnection();
                String queryup = "UPDATE clients SET CID=?, Name=?, Address=?, Contact=?, Email=? where CID='" + txt_clientID.getText() + "'";
                PreparedStatement pst = con.prepareStatement(queryup);

                pst.setString(1, txt_clientID.getText());
                pst.setString(2, txt_clientName.getText());
                pst.setString(3, txt_clientAddress.getText());
                pst.setString(4, txt_clientContactno.getText());
                pst.setString(5, txt_email.getText());

                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Updated Successfully");

            } catch (StringIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, "Please fill the required fields");
            }

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error Occured");
        }

    }//GEN-LAST:event_btn_updateActionPerformed

