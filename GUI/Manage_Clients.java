    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

            Connection con = DataBaseConnection.getDBconnection();

            String query = "SELECT CID, Name, Address, Contact, Email FROM clients where CID='" + txt_clientID.getText() + "'";

            st = con.prepareStatement(query);
            st = con.createStatement();
            rs = st.executeQuery(query);

            if (rs.next()) {
                txt_clientID.setText(txt_clientID.getText());

                txt_clientName.setText(rs.getString(2));

                txt_clientAddress.setText(rs.getString(3));

                txt_clientContactno.setText(rs.getString(4));
                txt_email.setText(rs.getString(5));

            } else {
                JOptionPane.showMessageDialog(null, "Employee Number Is Invalid.");
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

