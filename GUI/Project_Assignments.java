    public void assignshowuser() {

        ArrayList<Userassign> list = user_assign_List();
        DefaultTableModel model = (DefaultTableModel) assigntable.getModel();
        Object[] row = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getEID();
            row[1] = list.get(i).getStartdate();
            row[2] = list.get(i).getEnddate();
            row[3] = list.get(i).getPosition();
            row[4] = list.get(i).getStatus();

            model.addRow(row);

        }

    }

