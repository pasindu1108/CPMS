    private void btn_AssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AssignActionPerformed
        // TODO add your handling code here:
        String positionassign2 = cmb_possition.getSelectedItem().toString();
        String startdate = ((JTextField) jDateChooser1.getDateEditor().getUiComponent()).getText();
        String enddate = ((JTextField) jDateChooser2.getDateEditor().getUiComponent()).getText();

        String columnValue = null;
        String recordid = null;
        String newrecord = null;

        try {

            Connection con = DataBaseConnection.getDBconnection();
            String query = "select * from project_assign ORDER BY RecordID DESC LIMIT 1";
            PreparedStatement ps = con.prepareStatement(query);
            Statement statement = con.createStatement();
            ResultSet resultset = ps.executeQuery(query);

            while (resultset.next()) {

                columnValue = resultset.getString(1);
                System.out.print(columnValue);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Occured.");
        }

        if (columnValue == null) {
            newrecord = "Re100000";
        } else {
            String lastrecordid = columnValue.substring(2, 8);
            System.out.print(lastrecordid);
            int newid = Integer.parseInt(lastrecordid);
            int newrecordid = newid + 1;
            String id = Integer.toString(newrecordid);
            newrecord = "Re" + id;
        }

        try {

            Connection con = DataBaseConnection.getDBconnection();
            String query = "insert into project_assign( RecordID, EmployeeID, PID, Startdate, Enddate, Position, Status) values(?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);

            pst.setString(1, newrecord);
            pst.setString(2, txt_projectassign_empid.getText());
            pst.setString(3, txt_projectassifn_pid.getText());
            pst.setString(4, startdate);
            pst.setString(5, enddate);
            pst.setString(6, positionassign2);
            pst.setInt(7, 1);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Inserted successfully");

        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error Occured");
        }


    }//GEN-LAST:event_btn_AssignActionPerformed

