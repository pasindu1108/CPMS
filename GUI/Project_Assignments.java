    public ArrayList<Userassign> user_assign_List() {
        ArrayList<Userassign> userassignList = new ArrayList<>();
        try {
            Connection con = DataBaseConnection.getDBconnection();

            String query = "SELECT * FROM project_assign where PID='" + txt_assign_proid.getText() + "'";
            st = con.prepareStatement(query);
            st = con.createStatement();
            rs = st.executeQuery(query);

            Userassign userassign;
            while (rs.next()) {
                userassign = new Userassign(rs.getString("EmployeeID"), rs.getString("Startdate"), rs.getString("Enddate"), rs.getString("Position"), rs.getInt("Status"));

                userassignList.add(userassign);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return userassignList;
    }

